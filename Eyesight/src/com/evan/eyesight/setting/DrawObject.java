package com.evan.eyesight.setting;

public class DrawObject {

	public DrawObject(float f, float f1, int i, float f2, int j) {
		rst = -1;
		x = f;
		y = f1;
		nDir = i;
		dm = f2;
		strIndex = j;
	}

	public float dm;
	public int nDir;
	public int rst;
	public int strIndex;
	public float x;
	public float y;
}
